public class FirstSteps {
    public int sum(int a, int b) {
        return a + b;
    }
    public int mul(int a, int b) {
        return a * b;
    }
    public int div(int a, int b) {
        return a / b;
    }
    public int mod(int a, int b) {
        return a % b;
    }
    public boolean isEqual(int a, int b) {
        return a == b;
    }
    public boolean isGreater(int a, int b) {
        return a > b;
    }

}
